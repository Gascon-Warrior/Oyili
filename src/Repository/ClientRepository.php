    public function findFeaturedClients(): array
    {
        return $this->getEntityManager()->createQueryBuilder()
            ->select('c.slug', 'c.logo', 'c.company', 'v.vimeoId')
            ->from('App\Entity\Client', 'c')
            ->join('c.videos', 'v')
            ->where('v.is_cover = 1')
            ->getQuery()
            ->getResult();
    }

